// Считаем sum(1, n-1), при этом n таково, что последние sqrt(n) - 1 чисел не попали в блоки sqa.
// T(n) = 6 * (sqrt(n)-1) + 10 * sqrt(n) + 9 * 1 + 6 * (sqrt(n) - 2) + 4 =
// = 22 sqrt(n) - 5 <= 22 sqrt(n), для любого n
// c2 = 22.

// Считаем sum(0, n), при этом n - полный квадрат:
// T(n) = 10 * sqrt(n) + 4 >= 10 * sqrt(n)
// c1 = 10

// 0 <= 10 * sqrt(n) <= T(n) <= 22 * sqrt(n)
// => g(n) = sqrt(n)

